// ── Konami Code Sequence ───────────────────────────────────────

var konamiSequence = []string{
	"up", "up", "down", "down", "left", "right", "left", "right", "b", "a",
}

// konamiCheck returns true when the buffer matches the full Konami code.
func konamiCheck(buf []string) bool {
	if len(buf) < len(konamiSequence) {
		return false
	}
	tail := buf[len(buf)-len(konamiSequence):]
	for i, k := range konamiSequence {
		if tail[i] != k {
			return false
		}
	}
	return true
}

// ── Tick message for lyric animation ───────────────────────────

type rickTickMsg struct{}

func rickTick() tea.Cmd {
	return tea.Tick(1800*time.Millisecond, func(time.Time) tea.Msg {
		return rickTickMsg{}
	})
}

