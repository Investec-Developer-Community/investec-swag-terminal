// ── View ───────────────────────────────────────────────────────

func (m Model) rickrollView() string {
	width := m.width
	if width == 0 {
		width = 80
	}

	// Title
	title := m.theme.TextHighlight().Bold(true).Render("You've been Rick Rolled!")
	divider := m.theme.TextMuted().Render(strings.Repeat("─", min(width-4, 50)))

	// ASCII Rick in teal
	rick := m.theme.TextHighlight().Render(asciiRick)

	// Build lyrics up to current index
	var lyricLines string
	end := m.rickLyricIdx
	if end > len(rickLyrics) {
		end = len(rickLyrics)
	}

	// Show last ~6 visible lines for a karaoke scroll effect
	start := 0
	if end > 6 {
		start = end - 6
	}

	for i := start; i < end; i++ {
		line := rickLyrics[i]
		if i == end-1 && line != "" {
			// Current line highlighted
			lyricLines += m.theme.TextAccent().Bold(true).Render("  ♪ "+line) + "\n"
		} else if line == "" {
			lyricLines += "\n"
		} else {
			lyricLines += m.theme.TextBody().Render("    "+line) + "\n"
		}
	}

	// Hint to exit
	exitHint := m.theme.TextMuted().Render("press any key to return")

	body := lipgloss.JoinVertical(lipgloss.Left,
		"",
		title,
		divider,
		"",
		rick,
		"",
		lyricLines,
		"",
		exitHint,
		"",
	)

	padded := m.renderer.NewStyle().PaddingLeft(2).Render(body)
	outer := m.renderer.NewStyle().Width(width).Align(lipgloss.Center)
	return outer.Render(padded)
}

// ── Update ─────────────────────────────────────────────────────

func (m Model) rickrollUpdate(msg tea.Msg) (Model, tea.Cmd) {
	switch msg.(type) {
	case rickTickMsg:
		if m.rickLyricIdx < len(rickLyrics) {
			m.rickLyricIdx++
			return m, rickTick()
		}
		return m, nil

	case tea.KeyMsg:
		// Any key exits back to confirm screen
		m.konamiBuf = nil
		m.rickLyricIdx = 0
		m.page = confirmPage
		return m, nil
	}
	return m, nil
}
